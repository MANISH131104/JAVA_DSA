package Conditionals;

public class switchstatement {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1 :
                System.out.println("MANGO");
                break;

            case 2 :
                System.out.println("BANANA");
                break;

            case 3 :
                System.out.println("APPLE");
                break;

            default:
                System.out.println("WAKE UP");
        }

    }
}
