package BasicsOfJava;

public class Variablesint {
    public static void main(String[] args) {
        int x;
        x = 500;
        System.out.println(x);
        x = 400;
        System.out.println(x);
        System.out.println(x+1);
        x = x+5;
        System.out.println(x);
        x = x - 10;
        System.out.println(x);
    }
}
